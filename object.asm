; -------------------------------------------------
; Name:
;     BoxSetPos
; Brief:
;     將物體盒左下角置於某位置
; Uses:
;     eax esi
; Params:
;     box_ptr = 物體盒指標    (PTR BOX)
;           x = 左下角 x 座標 (DWORD)
;           y = 左下角 y 座標 (DWORD)
